       <li>2 cups all-purpose flour(maida)</li>
       <li>1/2 tsp salt</li>
       <li>water (as needed)</li>
       <br>
       <h2>For Filling</h2>
       <li>250g boneless chicken (minced)</li>
       <li>1 onion (finely chopped)</li>   
