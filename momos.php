<div class="steps">
    <h2>Steps</h2>
    <h2>Prepare the Dough</h2>
    <li>Mix flour and salt in a bowl</li>
    <li>Add water gradually and knead into a soft dough</li>
    <li>Cover and rest for 30 minutes</li>
    <br>
    <h2>Prepare the Filling</h2>
    <li>Heat oil in a pan</li>
    <li>Add ginger and garlic</li>
    <li>Add minced chicken and cook well</li>
    <li>Add onion, pepper, and salt</li>
    <li>Cook until dry and allow it to cool</li>
    <br>
    <h2>Shape the Momos</h2>
    <li>Divide dough into small balls</li>
    <li>Roll each ball into a thin circle</li>
    <li>Place a spoonful of filling in the center</li>
    <li>Fold and seal the edges</li>
    <br>
    <h2>Steam the Momos</h2>
    <li>Grease a steamer plate</li>
    <li>Arrange the momos</li>
    <li>Steam for 10 -12 minutes</li>
    <li>Serve hot with spicy momos chutney</li>
   </ol>
