    <h2>Prepare the Filling</h2>
    <li>Heat oil in a pan</li>
    <li>Add garlic and sauce </li>
    <li>Add chicken and cook until done</li>
    <li>Add onion, cabbage, and carrot</li>
    <li>Stir-fry for a few minutes</li>
    <li>Add soy sauce, pepper, and salt</li>
    <li>Mix well and allow the filling to cool</li>
    <br>
    <h2>Roll the Spring Rolls</h2>
    <li>Place a spring roll sheet on a flat surface</li>
    <li>Add the chicken filling</li>
    <li>Fold the sides inward</li>
    <li>Roll tightly</li>
    <li>Seal the edges with the maida paste</li>
    <br>
    <h2>Fry the Spring Rolls</h2>
    <li>Heat oil in a deep pan</li>
    <li>Fry the rolls until golden brown</li>
    <li>Remove and drain excess oil</li>
    <li>Serve hot with chilli sauce</li>
    <li>Serve hot</li>
