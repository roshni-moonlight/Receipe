    <li>Mix well and shape into patties</li>
    <li>Shallow fry until golden brown on both sides</li>
    <br>
    <h2>Assemble the Burger</h2>
    <li>Cut the burger bun into halves</li>
    <li>Toast with butter</li>
    <li>Spread mayonnaise</li>
