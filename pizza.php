       <li>200g Boneless Chicken</li>
       <li>1 tsp curd</li>
       <li>1 tsp chilli powder</li>
       <li>1/2 tsp Turmeric powder</li>
       <li>1 tsp ginger-garlic paste</li>
       <li>Salt</li>
       <br>
       <h2>Additional Toppings</h2>
       <li>1/2 cup pizza sauce</li>
       <li>1 cup mozzarella cheese</li>
       <li>1 onion(sliced)</li>
       <li>1 capsicum(sliced)</li>
       <li>oregano</li>
       <li>chilli flakes</li>
    </ul>
</div>
   
   <ol>
<div class="steps2">
    <h2>Steps</h2>
    <h2>Prepare Chicken Tikka</h2>
    <li>Marinate chicken with curd, chilli powder, turmeric, ginger-garlic paste, and salt<li>
    <li>Rest for 30 minutes</li>
    <li>Grill or fry the chicken until cooked</li>
    <br>
    <h2>Prepare the Pizza</h2>
    <li>Roll the prepared dough</li>
    <li>Spread pizza sauce</li>
    <li>Add mozzarella cheese</li>
    <li>Add chicken tikka pieces</li>
    <li>Add onion and capsicum slices</li>
    <li>Sprinkle oregano and chilli flakes</li>
    <li>Bake at 220°C for 15–20 minutes.</li>
    <li>Slice and serve hot</li>
