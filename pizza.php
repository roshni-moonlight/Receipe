       <li>2 cups all-purpose flour(maida)</li>
       <li>1 tsp yeast</li>
       <li>1 tsp sugar</li>
       <li>1/2 tsp salt</li>
       <li>3/4 cup warm water</li>   
       <li>2 tsp olive oil</li>
       <br>
       <h2>For Topping</h2>
       <li>1/2 cup pizza sauce</li>
       <li>1 cup mozzarella cheese</li>
       <li>Fresh basil leaves</li>
       <li>1 tsp oregano</li>
    </ul>
</div>
   
   <ol>
<div class="steps">
    <h2>Steps</h2>
    <h2>Prepare the Dough</h2>
    <li>Mix yeast, sugar, and warm water</li>
    <li>Leave for 10 minutes until frothy</li>
    <li>Add flour, salt, and olive oil</li>
    <li>Knead into a soft dough</li>
    <li>Cover and let it rise for 1 hour</li>
    <br>
    <h2>Prepare the Pizza</h2>
    <li>Roll the dough into a round shape</li>
    <li>Spread pizza sauce evenly</li>
    <li>Add mozzarella cheese</li>
    <li>Place bassil leaves on top</li>
    <li>Sprinkle oregano</li>
    <li>Bake at 220°C for 15–20 minutes</li>
    <li>Slice and serve hot</li>
   </ol>
