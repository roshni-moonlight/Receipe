    <h2>Steps</h2>
    <h2>Prepare the Filling</h2>
    <li>Shred the cooked chicken</li>
    <li>Mix chicken with a little mayonnaise, salt, and pepper</li>
    <br>
    <h2>Assemble the Sandwich</h2>
    <li>Toast the bread slices lightly</li>
    <li>Spread mayonnaise on each slice</li>
    <li>Place lettuce leaves</li>
    <li>Add the chicken mixture</li>
    <li>Add tomato and onion slices</li>
    <li>Place cheese slices</li>
    <li>Stack with another bread layer if desired</li>
    <li>Cut diagonally into triangles</li>
    <li>Secure with toothpicks and serve</li>
