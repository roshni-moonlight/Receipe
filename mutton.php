       <li>2 Onions(chopped)</li>
       <li>1 Tomato(choppedd)</li>
       <li>1 tbsp Ginger-Garlic Paste</li>
       <li>1 tsp Black pepper powder</li>
       <li>1 tsp Cumin powder</li>
       <li>1/2 tsp Turmeric powder</li>
       <li>Curry leaves</li>
       <li>Coriander leaves</li>
       <li>Salt</li>
       <li>1 tsp Oil</li>
       <li> 4-5 cups water</li>
    </ul>
</div>
   
   <ol>
<div class="steps3">
    <h2>Steps</h2>
    <li>Wash the mutton pieces thoroughly</li>
    <li>Heat oil in a pressure cooker</li>
    <li>Add chopped onions and saute until soft</li>
    <li>Add ginger-garlic paste and cook for 2 minutes</li>
    <li>Add tomatoes and cook until mushy</li>
    <li>Add turmeric powder, cumin powder, pepper powder, and salt</li>
    <li>Add the mutton pieces and mix well</li>
    <li>Pour 4-5 cups of water</li>
    <li>Pressure cook for 6-8 whistels or until the mutton becomes tender</li>
    <li>Open the cooker and simmer for 10 minutes</li>
    <li>Garnish with coriander leaves and curry leaves</li>
    <li>Serve hot with pepper sprinkled on top</li>
   </ol>
