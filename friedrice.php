        <h2>For Rice</h2>
    <ul>
       <li>2 cups cooked rice (cooled)</li>
       <br>
       <h2>For Fried Rice</h2>
       <li>3 eggs</li>
       <li>1 onion (chopped)</li>
       <li>1 carrot(finely chopped)</li>
       <li>1/2 cup cabbage (shredded)</li>
       <li>1 capsicum (chopped)</li>   
       <li>1 tsp soy sauce</li>
       <li>1 tsp pepper powder</li>
       <li>2 tsp oil</li>
       <li>salt</li>
       <li>Spring onion for garnish</li>
    </ul>
</div>
   
   <ol>
<div class="steps2">
    <h2>Steps</h2>
    <h2>Prepare the Eggs</h2>
    <li>Break the eggs into a bowl</li>
    <li>Add a pinch of salt and beat well</li>
    <li>Heat oil in a work</li>
    <li>Pour the eggs and scramble them</li>
    <li>Keep aside</li>
    <br>
    <h2>Prepare the Fried Rice</h2>
    <li>In the same work, add onion and saute</li>
    <li>Add carrot, cabbage, and capsicum</li>
    <li>Stir-fry on high flame for 2-3 minutes</li>
    <li>Add the scrambled eggs</li>
    <li>Add soy sauce, peppeer, and salt</li>
    <li>Add cooked rice</li>
    <li>Toss everything well for 3-4 minutes</li>
    <li>Garnish with spring onions</li>
    <li>Serve hot</li>
   </ol>
