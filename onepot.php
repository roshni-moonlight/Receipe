       <li>1/2 cup moong dal</li>
       <li>1 tsp cumin seeds</li>
       <li>1 tsp black pepper</li>
       <li>10 cashews</li>   
       <li>Curry leaves</li>
       <li>2 tsp ghee</li>
       <li>4 cups water</li>
       <li>salt</li>
    </ul>
</div>
   
   <ol>
<div class="steps2">
    <h2>Steps</h2>
    <li>Wash millet and moong dal</li>
    <li>Dry roast moong dal lightly</li>
    <li>Add millet and dal to a cooker</li>
    <li>add water and salt</li>
    <li>Pressure cook for 4 whistles</li>
    <li>Heat ghee in a pan</li>
    <li>Fry cashews, cumin, pepper, and curry leaves</li>
    <li>Add the seasoning to the cooked pongal</li>
    <li>Mix well</li>
    <li>Serve hot with chutnery or sambar</li>
   </ol>
