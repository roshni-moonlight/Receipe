   <ol>
<div class="steps">
    <h2>Steps</h2>
    <li>Dry roast oats for 2-3 minutes and keep aside</li>
    <li>Heat oil in a pan</li>
    <li>Add mustard seeds and curry leaves</li>
    <li>Add onions and green chillies, saute well</li>
    <li>Add carrots and peas</li>
    <li>Cook vegetables for 3-4 minutes</li>
    <li>Pour water and add salt</li>
    <li>Once water boils, add oats slowly</li>
    <li>Stir continuously to avoid lumps</li>
    <li>Cook for 5 minutes</li>
    <li>Serve hot</li>
   </ol>
