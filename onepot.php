       <li>1 onion(chopped)</li>
       <li>1 carrot(chopped)</li>
       <li>1/4 cup green peas</li>
       <li>2 green chillies</li>   
       <li>1 tsp mustard seeds</li>
       <li>Curry leaves</li>
       <li>2 cups water</li>
       <li>salt</li>
       <li>2 tsp oil</li>
    </ul>
