    </div>
    <div class="ingredients">
        <h2>Ingredients</h2>
        <h2>For Chicken Filling</h2>
    <ul>
       <li>250g boneless chicken</li>
       <li>2 tsp curd</li>
       <li>1 tsp chilli powder</li>
       <li>1 tsp pepper powder</li>
       <li>1 tsp ginger-garlic paste</li>   
       <li>1/2 tsp turmeric powder</li>
       <li>salt</li>
       <li>1 tsp oil</li>
       <br>
       <h2>For Shawarma Wrap</h2>
       <li>2 kuboos/pita bread</li>
       <li>lettuce leaves</li>
       <li>1 tomato (sliced)</li>
       <li>1 onion (sliced)</li>
       <br>
       <h2>For Garlic Sauce</h2>
       <li>3 tsp mayonnaise</li>
       <li>1 tsp garlic paste</li>
    </ul>
</div>
   
   <ol>
<div class="steps">
    <h2>Steps</h2>
    <h2>Prepare the Chicken</h2>
    <li>Mix curd, chilli powder, pepper, turmeric, ginger-garlic paste, and salt</li>
    <li>Marinate the chicken for 30 minutes</li>
    <li>Heat oil in a pan</li>
    <li>Cook the chicken until tender and slightly charred</li>
    <li>Slice the chicken into the strips</li>
    <br>
    <h2>Prepare the Garlic Sauce</h2>
    <li>Mix mayonnaise and garlic paste</li>
    <li>Stir well until smooth </li>
    <br>
    <h2>Assemble the Shawarma</h2>
    <li>Place the kuboos on a flat surface</li>
    <li>Spread garlic sauce evenly</li>
    <li>Add lettuce, onion, and tomato</li>
    <li>Place the cooked chicken strips</li>
    <li>Roll tightly into a wrap</li>
    <li>Toast lightly on a pan</li>
    <li>Cut into halves and serve</li>
   </ol>
