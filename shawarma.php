    <div class="ingredients2">
        <h2>Ingredients</h2>
        <h2>For Paneer Filling</h2>
    <ul>
       <li>250g Paneer cubes</li>
       <li>2 tsp curd</li>
       <li>1 tsp chilli powder</li>
       <li>1/2 tsp turmeric powder</li>
       <li>1 tsp ginger-garlic paste</li>   
       <li>1/2 tsp turmeric powder</li>
       <li>salt</li>
       <li>1 tsp oil</li>
       <br>
       <h2>For Shawarma Wrap</h2>
       <li>2 kuboos/pita bread</li>
       <li>lettuce leaves</li>
       <li>1 tomato (sliced)</li>
       <li>1 onion (sliced)</li>
       <br>
        <h2>For Sauce</h2>
       <li>3 tsp mayonnaise</li>
       <li>1 tsp garlic paste</li>
    </ul>
</div>
   
   <ol>
<div class="steps2">
    <h2>Steps</h2>
    <h2>Prepare the paneer</h2>
    <li>Mix curd, chilli powder, pepper, turmeric, ginger-garlic paste, and salt</li>
    <li>Marinate paneer cubes for 20 minutes</li>
    <li>Heat oil in a pan</li>
    <li>Cook paneer until lightly golden</li>
    <li>Cut into strips if needed</li>
    <br>
    <h2>Prepare the Sauce</h2>
    <li>Mix mayonnaise and garlic paste</li>
    <li>Stir until creamy </li>
    <br>
    <h2>Assemble the Shawarma</h2>
    <li>Spread garlic sauce on the kuboos</li>
    <li>Add lettuce, onion, and tomato</li>
    <li>Place cooked paneer pieces</li>
    <li>Roll tightly </li>
    <li>Toast for 2 minutes on a pan</li>
    <li>Cut and serve hot</li>
   </ol>
