   <ol>
<div class="steps2">
    <h2>Steps</h2>
    <li>Wash and marinate mutton with curd and spices for 1 hour </li>
    <li>Pressure cook the mutton until tender</li>
    <li>Fry onions in ghee until golden</li>
    <li>Add ginger-garlic paste and tomatoes</li>
    <li>Add cooked mutton and masala</li>
    <li>Cook soaked rice separately until partially done</li>
    <li>Layer rice and mutton mixture</li>
    <li>Cover tightly and cook on low flame</li>
    <li>Allow it to rest for 10 minutes</li>
    <li>Serve with onion raita</li>
   </ol>
