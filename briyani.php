<div class="title">◼️Chicken Briyani</div>
<div class="align">
    <div class="image">
        <img src="./chickenbriyani.jpg" alt="">
    </div>
    <div class="ingredients">
        <h2>Ingredients</h2>
    <ul>
       <li>500g chicken</li>
       <li>2 Cups basmati Rice</li>
       <li>2 Onions(Sliced)</li>
       <li>2 Tomatoes(Chopped)</li>
       <li>2 tbsp Ginger-Garlic Paste</li>
       <li>2 Green Chillies</li>
       <li>Mint Leaves</li>
       <li>Coriander Leaves</li>
       <li>1 Cup Curd</li>
       <li>Briyani Masala</li>
       <li>Turmeric Powder</li>
       <li>Chilli Powder</li>
       <li>Salt</li>
       <li>Oil/Ghee</li>
    </ul>
</div>
   
   <ol>
<div class="steps">
    <h2>Steps</h2>
    <li>Wash and soak the basmati rice for 30 minutes.</li>
    <li>Marinate chicken with curd, turmeric, chilli powder, salt, and biryani masala.</li>
    <li>Heat oil and fry onions until golden brown</li>
    <li>Add ginger-garlic paste and sauté.</li>
    <li>Add tomatoes and cook until soft.</li>
    <li>Add marinated chicken and cook well.</li>
    <li>Add mint and coriander leaves.</li>
    <li>Cook rice separately until 70% done.</li>
    <li>Layer rice over the chicken mixture.</li>
    <li>Cover and cook on low flame for 20–25 minutes.</li>
    <li>Serve hot with raita.</li>
   </ol>
</div></div><hr>



